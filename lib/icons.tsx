// Size map for consistent icon sizing
const sizeMap = {
  sm: 'w-4 h-4',
  md: 'w-5 h-5',
  lg: 'w-6 h-6',
  xl: 'w-8 h-8',
};

type IconSize = keyof typeof sizeMap;

interface IconProps {
  className?: string;
  size?: IconSize;
}

// Extended props for icons that need to spread additional SVG attributes
type ExtendedIconProps = IconProps & React.SVGProps<SVGSVGElement>;

// Helper to compute className from size and className props
const getIconClassName = (className?: string, size?: IconSize, defaultSize = 'md'): string => {
  if (className) return className;
  return sizeMap[size || (defaultSize as IconSize)];
};

